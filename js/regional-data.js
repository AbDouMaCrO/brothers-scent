// Seed into localStorage for dashboard use
(function seedRegional() {
  const existing = JSON.parse(localStorage.getItem('ls_products') || '[]');
  const existingIds = new Set(existing.map(p => p.id));

  const allRegional = [...REGIONAL_DATA.uae, ...REGIONAL_DATA.saudi];

  const cats = JSON.parse(localStorage.getItem('ls_categories') || '[]');
  const catSlugs = new Set(cats.map(c => c.slug));

  if (!catSlugs.has('uae-trending')) {
    cats.push({ id: 'cat_uae', name: 'UAE Trending', slug: 'uae-trending', icon: '🇦🇪', active: true });
  }
  if (!catSlugs.has('saudi-trending')) {
    cats.push({ id: 'cat_sa', name: 'Saudi Arabia Trending', slug: 'saudi-trending', icon: '🇸🇦', active: true });
  }
  localStorage.setItem('ls_categories', JSON.stringify(cats));

  const uaeCatId = cats.find(c => c.slug === 'uae-trending')?.id || 'cat_uae';
  const saCatId  = cats.find(c => c.slug === 'saudi-trending')?.id || 'cat_sa';

  let added = 0;
  allRegional.forEach(p => {
    if (!existingIds.has(p.id)) {
      existing.push({
        id: p.id, name: p.name, brand: p.brand,
        categoryId: p.region === 'uae' ? uaeCatId : saCatId,
        size: p.size, price: p.price, stock: p.stock,
        badge: p.badge, active: true,
        createdAt: new Date().toISOString()
      });
      added++;
    }
  });
  if (added > 0) localStorage.setItem('ls_products', JSON.stringify(existing));
})();
